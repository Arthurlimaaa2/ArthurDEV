
// Example of a string data type, used for text
let exampleString = "Hello, World!";

// Example of a number data type, used for numbers
let exampleNumber = 42;

// Example of a boolean data type, used for true/false values
let exampleBoolean = true;

// Example of an undefined data type, used for variables that have not been initialized
let exampleUndefined;

// Example of a bigint data type, used for very large numbers
let exampleBigInt = 123456789012345678901234567890n;

// Example of a symbol data type, used for unique and immutable values
let exampleSymbol = Symbol('unique');

// Example of a null data type, representing "no value"
let exampleNull = null;

console.log(exampleString);
console.log(exampleNumber);
console.log(exampleBoolean);
console.log(exampleUndefined);
console.log(exampleBigInt);
console.log(exampleSymbol);
console.log(exampleNull);
